angka2_list = [1,2,3,4,5]

for i in angka2_list : 
    print(f"i sekarang -> {i}")
print("perulangan 1 selesai\n")

angka2_range = range(5)

for i in angka2_range :
    print(f"i sekarang -> {i} ")
print("perulangan 2 selesai\n")

angka2_range = range(1,5)

for i in angka2_range :
    print(f"saya keren")
